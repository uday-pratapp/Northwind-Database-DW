
--------------------------------------------------------------- CREATING ALL TABLES -------------------------------------------------------------------------------------

CREATE PROCEDURE CREATE_ALL
AS BEGIN
EXEC LND_CreateTables;
EXEC STG_CreateTables;
EXEC DW_CreateTables;
END
---------------------------------------------------------------LOAD ALL DATA---------------------------------------------------------------------------------------------

CREATE PROCEDURE LOAD_ALL
AS BEGIN
EXEC LND_LoadData;
EXEC STG_LoadData;
EXEC DW_LoadData;
END

-------------------------------------------------------------------------DROP ALL TABLES----------------------------------------------------------------------------------------

EXEC DROP_ALL;

--------------------------------------------------------------------DROP ALL PROCEDURES -------------------------------------------------------------------------------------------

EXEC DROP_ALL_PROCS;